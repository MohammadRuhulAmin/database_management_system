    What is candidate key ? 
    Candidate key is a single key or a group of multiple keys that uniquely identify rows in a table. 
    A candidate key is a column or a combination of columns that uniquely identifies each row in a table. 
    It is used to ensure that there are no duplicate or ambiguous records in a table.


    <passport_no,contact_no,email_id,birth_registration,nid,licence> are the set of candidate key. 
    We always consider primary key from the candidate key. after selecting the primary key, we 
    consider the other key inside candidate set is called alternative keys.
    